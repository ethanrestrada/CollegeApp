CREATE DATABASE cbp;

CREATE TABLE roles(
  id INT AUTO_INCREMENT PRIMARY KEY,
  rol VARCHAR(255)
);

CREATE TABLE grados(
  id INT AUTO_INCREMENT PRIMARY KEY,
  grado VARCHAR(255),
  seccion VARCHAR(255)
);

CREATE TABLE catedras(
  id INT AUTO_INCREMENT PRIMARY KEY,
  catedra VARCHAR(255)
);

CREATE TABLE coordinadores(
  id INT AUTO_INCREMENT PRIMARY KEY,
  nombre VARCHAR(255),
  apellido VARCHAR(255),
  correo VARCHAR(255),
  contrasenia VARCHAR(255),
  estado VARCHAR(255),
  id_rol INT,
  FOREIGN KEY (id_rol) REFERENCES roles(id)
);

ALTER TABLE coordinadores AUTO_INCREMENT = 1001

CREATE TABLE maestros(
  id INT AUTO_INCREMENT PRIMARY KEY,
  nombre VARCHAR(255),
  apellido VARCHAR(255),
  correo VARCHAR(255),
  contrasenia VARCHAR(255),
  contacto VARCHAR(255),
  estado VARCHAR(255),
  id_rol INT,
  FOREIGN KEY (id_rol) REFERENCES roles(id)
);

ALTER TABLE maestros AUTO_INCREMENT = 3001

CREATE TABLE alumnos(
  id INT AUTO_INCREMENT PRIMARY KEY,
  nombre VARCHAR(255),
  apellido VARCHAR(255),
  id_grado INT,
  correo VARCHAR(255),
  contrasenia VARCHAR(255),
  codigo VARCHAR(255),
  estado VARCHAR(255),
  id_rol INT,
  FOREIGN KEY (id_rol) REFERENCES roles(id),
  FOREIGN KEY (id_grado) REFERENCES grados(id)
);

ALTER TABLE alumnos AUTO_INCREMENT = 5001

CREATE TABLE clase(
  id INT AUTO_INCREMENT PRIMARY KEY,
  id_catedra INT,
  id_grado INT,
  id_maestro INT,
  FOREIGN KEY (id_catedra) REFERENCES catedras(id),
  FOREIGN KEY (id_grado) REFERENCES grados(id),
  FOREIGN KEY (id_maestro) REFERENCES maestros(id)
);

CREATE TABLE tareas(
  id INT AUTO_INCREMENT PRIMARY KEY,
  id_clase INT,
  titulo VARCHAR(255),
  code_file VARCHAR(255),
  name_file VARCHAR(255),
  updated_at VARCHAR(255),
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  entrega DATE,
  tipo VARCHAR(255),
  descripcion VARCHAR(1275),
  FOREIGN KEY (id_clase) REFERENCES clase(id)
);

CREATE TABLE chat(
  id INT AUTO_INCREMENT PRIMARY KEY,
  incoming_id INT,
  outgoing_id INT,
  msg VARCHAR(1275)
);

CREATE TABLE entregas(
  id INT PRIMARY KEY AUTO_INCREMENT,
  id_tarea INT,
  id_alumno INT,
  code_file VARCHAR(255),
  name_file VARCHAR(255),
  estado VARCHAR(255),
  turnin_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY (id_tarea) REFERENCES tareas(id),
  FOREIGN KEY (id_alumno) REFERENCES alumnos(id)
);

